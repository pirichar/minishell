#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*first;
	t_list	*lst1;

	lst1 = lst;
	first = NULL;
	while (lst1)
	{
		new = ft_lstnew(f(lst1->content));
		if (new == NULL)
		{
			ft_lstclear(&first, del);
			return (NULL);
		}
		ft_lstadd_back(&first, new);
		lst1 = lst1->next;
	}
	return (first);
}
