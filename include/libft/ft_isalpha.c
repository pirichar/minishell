#include "libft.h"

int	ft_isalpha(int c)
{
	if (c < 0 || c > 255)
		return (0);
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}
